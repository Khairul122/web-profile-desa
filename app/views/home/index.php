<script>
    // Animasi tambahan menggunakan Anime.js
    document.addEventListener('DOMContentLoaded', function() {
        // Animasi untuk kartu layanan
        anime({
            targets: '.animate-card',
            translateY: [-20, 0],
            opacity: [0, 1],
            scale: [0.9, 1],
            delay: anime.stagger(200, {start: 300}),
            duration: 1000,
            easing: 'easeOutElastic(1, .8)'
        });
        
        // Animasi untuk ikon
        anime({
            targets: '.animate-icon',
            scale: [{value: 0.8}, {value: 1.1}, {value: 1}],
            delay: anime.stagger(200, {start: 500}),
            duration: 1500,
            easing: 'easeInOutQuad',
            loop: false
        });
        
        // Animasi untuk elemen berita
        anime({
            targets: '.card',
            translateY: [50, 0],
            opacity: [0, 1],
            delay: anime.stagger(100),
            duration: 800,
            easing: 'easeOutCubic'
        });
        
        // Animasi untuk statistik
        anime({
            targets: '.display-5',
            innerHTML: [0, el => parseInt(el.textContent)],
            round: 1,
            delay: anime.stagger(200, {start: 500}),
            duration: 2000,
            easing: 'easeOutQuad'
        });
    });
</script>